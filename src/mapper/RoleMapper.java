package mapper;

import java.util.List;

import entity.Role;

/**
 * 角色表的接口类
 * @author 晓煜
 * @date2018年3月26日下午3:58:37
 * @version
 * @explain
 */
public interface RoleMapper {
	/**
	 * 添加角色
	 * @param role 角色实体对象
	 */
	public void saveRole(Role role);
	/**
	 * 删除角色
	 * @param role 角色实体类对象
	 */
	public void removeRole(Role role);
	/**
	 * 修改角色表信息根据传入的实体对象进行修改
	 * @param role  角色的实体类对象
	 */
	public void updateRole(Role role);
	/**
	 * 查询所有角色
	 * @return 返回角色对象的集合
	 */
	public List<Role>listRole();
}
