package entity;
/**
 * 工作流程表实体类
 * @WorkFlow.java
 * @author 谭加益
 * @time 2018年3月26日下午5:06:39
 */
public class WorkFlow {
	private int id;//工作流程表id
	private String starttime;//开始时间
	private String endtime;//结束时间
	private int titaltime;//时间差
	private int project_id;//项目id
	private int art_id;//技术人员id
	
	@Override
	public String toString() {
		return "WorkFlow [id=" + id + ", starttime=" + starttime + ", endtime=" + endtime + ", titaltime=" + titaltime
				+ ", project_id=" + project_id + ", art_id=" + art_id + "]";
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getStarttime() {
		return starttime;
	}
	public void setStarttime(String starttime) {
		this.starttime = starttime;
	}
	public String getEndtime() {
		return endtime;
	}
	public void setEndtime(String endtime) {
		this.endtime = endtime;
	}
	public int getTitaltime() {
		return titaltime;
	}
	public void setTitaltime(int titaltime) {
		this.titaltime = titaltime;
	}
	public int getProject_id() {
		return project_id;
	}
	public void setProject_id(int project_id) {
		this.project_id = project_id;
	}
	public int getArt_id() {
		return art_id;
	}
	public void setArt_id(int art_id) {
		this.art_id = art_id;
	}
	
}
